package kr.co.makeit.hanium.domain;


public class Member {
	private double memLat;
	private double memLon;
	private double memName;
	public double getMemLat() {
		return memLat;
	}
	public void setMemLat(double memLat) {
		this.memLat = memLat;
	}
	public double getMemLon() {
		return memLon;
	}
	public void setMemLon(double memLon) {
		this.memLon = memLon;
	}
	public double getMemName() {
		return memName;
	}
	public void setMemName(double memName) {
		this.memName = memName;
	}
}
